#include <iostream>
using namespace std;

int main() {
    // Arithmetic Operator
    int a = 6;
    int b = 3;
    
    cout<<"This is Addition: "<<a+b<<endl;
    cout<<"This is Subtraction: "<<a-b<<endl;
    cout<<"This is Multiplication: "<<a*b<<endl;
    cout<<"This is Division: "<<a/b<<endl;
    cout<<"This is Modulo: "<<a%b<<endl;
    cout<<"The increases: "<<a++<<endl;
    cout<<"The decreases: "<<a--<<endl;

    return 0;
}